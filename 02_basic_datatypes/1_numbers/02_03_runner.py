'''

If a runner runs 10 miles in 30 minutes and 30 seconds,
What is his/her average speed in kilometers per hour? (Tip: 1 mile = 1.6 km)

'''

run_miles = 10
run_time = 30.5

run_miles_converter = run_miles * 1.6
run_time_converter = run_time / 60

result = run_miles_converter * run_time_converter

print(result)

